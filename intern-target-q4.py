# 4) Você está em uma sala com três interruptores, cada um conectado a uma lâmpada em uma sala diferente. Você não pode ver as lâmpadas da sala em que está, mas pode ligar e desligar os interruptores quantas vezes quiser. Seu objetivo é descobrir qual interruptor controla qual lâmpada.

# Como você faria para descobrir, usando apenas duas idas até uma das salas das lâmpadas, qual interruptor controla cada lâmpada?  

#     Ligue um interruptor e aguarde alguns minutos para aquecer a lâmpada.
#     Desligue o interruptor.
#     Ligue outro interruptor.
#     Entre na sala.

#     Agora, temos três cenários possíveis:

#     Se a lâmpada estiver acesa, então o interruptor que você acabou de ligar controla essa lâmpada.
#     Se a lâmpada estiver apagada, mas ainda quente, então o primeiro interruptor controla essa lâmpada.
#     Se a lâmpada estiver apagada e fria, então o interruptor que você não tocou controla essa lâmpada.